extension UIColor {
    
    public class func softOrange() -> UIColor {
        return UIColor(red: 249.0 / 255.0, green: 164.0 / 255.0, blue: 94.0 / 255.0, alpha: 1.0)
    }
    
    public class func darkOrange() -> UIColor {
        return UIColor(red: 255.0 / 255.0, green: 131.0 / 255.0, blue: 62.0 / 255.0, alpha: 1.0)
    }
    
    public class func softWhite() -> UIColor {
        return UIColor(red: 249.0 / 255.0, green: 246.0 / 255.0, blue: 244.0 / 255.0, alpha: 1.0)
    }
    
    public class func darkGrey() -> UIColor {
        return UIColor(red: 180.0 / 255.0, green: 180.0 / 255.0, blue: 180.0 / 255.0, alpha: 1.0)
    }
    
    public class func softGrey() -> UIColor {
        return UIColor(red: 218.0 / 255.0, green: 216.0 / 255.0, blue: 215.0 / 255.0, alpha: 1.0)
    }
    
    public class func textColor() -> UIColor {
        return UIColor(red: 74 / 255.0, green: 73 / 255.0, blue: 72 / 255.0, alpha: 1.0)
    }
}
